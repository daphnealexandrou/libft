#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*c1;
	unsigned char	*c2;
	size_t			i;

	c1 = (unsigned char *)s1;
	c2 = (unsigned char *)s2;
	i = 0;
	while (i < n && *c1 == *c2)
	{
		c1++;
		c2++;
		i++;
	}
	if (i == n)
		return (0);
	else
		return (*c1 - *c2);
}
